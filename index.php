  <h1 class="boldtxt">Background</h1>
  <p class="ptxt">On March 18, 2020, President Trump signed into law the Families First Coronavirus Response Act
    (FFCRA),
    which provided additional flexibility for state unemployment insurance agencies and additional administrative
    funding to respond to the COVID-19 pandemic. The Coronavirus Aid, Relief, and Economic Security (CARES) Act was
    signed into law on March 27. It expands states’ ability to provide unemployment insurance for many workers impacted
    by the COVID-19 pandemic, including for workers who are not ordinarily eligible for unemployment benefits.
    For more information, please refer to the resources available below.</p>

  <h1 class="boldtxt">If I am eligible for Pandemic Unemployment Assistance, do I need to first apply for unemployment
    insurance?</h1>
  <p class="ptxt">States must have a process for determining that Pandemic Unemployment Assistance (PUA) applicants are
    ineligible for regular unemployment benefits, which may not include filing a regular claim as a first step. States
    are not required to take and adjudicate a full claim for regular unemployment insurance benefits to meet this
    requirement. While states are not prohibited from taking a full claim, to facilitate expedited claims processing the
    U.S. Department of Labor has discouraged states from doing so. Individuals should apply using the state’s PUA
    application process and,
    in states that have not yet established that process, must wait until it is established.</p>

  <h1 class="boldtxt">Where Do I File for Unemployment Insurance?</h1>
  <p class="ptxt">Unemployment insurance is a joint state-federal program that provides cash benefits to eligible
    workers. Each state administers a separate unemployment insurance program,
    but all states follow the same guidelines established by federal law.</p>
